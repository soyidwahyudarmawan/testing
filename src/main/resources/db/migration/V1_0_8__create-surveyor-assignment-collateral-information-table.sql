CREATE TABLE IF NOT EXISTS surveyor_assignment_collateral_information (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assignment_id BIGINT NOT NULL,
  brand_code VARCHAR(255) NULL,
  brand_name VARCHAR(255) NULL,
  model_code VARCHAR(255) NULL,
  model_name VARCHAR(255) NULL,
  variant_code VARCHAR(255) NULL,
  variant_name VARCHAR(255) NULL,
  year_made INT NULL,
  license_number VARCHAR(255) NULL,
  vehicle_registration_photo VARCHAR(255) NULL,
  vehicle_tax_notice_photo VARCHAR(255) NULL,
  vehicle_left_side_photo VARCHAR(255) NULL,
  vehicle_right_side_photo VARCHAR(255) NULL,
  vehicle_front_side_photo VARCHAR(255) NULL,
  vehicle_back_side_photo VARCHAR(255) NULL,
  vehicle_dashboard_side_photo VARCHAR(255) NULL,
  active BOOLEAN DEFAULT TRUE,
  created_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  created_user VARCHAR(255) DEFAULT 'system',
  updated_date TIMESTAMP NULL,
  updated_user VARCHAR(255),
  CONSTRAINT fk_surveyor_assignment_collateral_information_surveyor_assignment_assignment_id FOREIGN KEY (assignment_id) REFERENCES surveyor_assignment (id)
);
