CREATE TABLE IF NOT EXISTS surveyor_assignment_emergency_contact (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assignment_id BIGINT NOT NULL,
  customer_relationship VARCHAR(255) NULL,
  name VARCHAR(255) NULL,
  address TEXT NULL,
  province_code VARCHAR(255) NULL,
  province_name VARCHAR(255) NULL,
  city_code VARCHAR(255) NULL,
  city_name VARCHAR(255) NULL,
  district_code VARCHAR(255) NULL,
  district_name VARCHAR(255) NULL,
  subdistrict_code VARCHAR(255) NULL,
  subdistrict_name VARCHAR(255) NULL,
  zipcode VARCHAR(255) NULL,
  phone_number VARCHAR(255) NULL,
  active BOOLEAN DEFAULT TRUE,
  created_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  created_user VARCHAR(255) DEFAULT 'system',
  updated_date TIMESTAMP NULL,
  updated_user VARCHAR(255),
  CONSTRAINT fk_surveyor_assignment_physical_document_surveyor_assignment_assignment_id FOREIGN KEY (assignment_id) REFERENCES surveyor_assignment (id)
);