CREATE TABLE IF NOT EXISTS applicant (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id UUID NOT NULL,
  full_name VARCHAR(255) NOT NULL,
  phone_number VARCHAR(255) NOT NULL,
  identity_number VARCHAR(255) NOT NULL,
  npwp_number VARCHAR(255) NOT NULL,
  marital_status VARCHAR(255) NOT NULL,
  gender VARCHAR(255) NOT NULL,
  date_of_birth DATE NOT NULL,
  place_of_birth VARCHAR(255) NOT NULL,
  nationality VARCHAR(255) NOT NULL,
  monthly_income NUMERIC(17,2) NOT NULL,
  address TEXT NOT NULL,
  additional_address TEXT NULL,
  province_code VARCHAR(255) NOT NULL,
  province_name VARCHAR(255) NOT NULL,
  city_code VARCHAR(255) NOT NULL,
  city_name VARCHAR(255) NOT NULL,
  district_code VARCHAR(255) NOT NULL,
  district_name VARCHAR(255) NOT NULL,
  subdistrict_code VARCHAR(255) NOT NULL,
  subdistrict_name VARCHAR(255) NOT NULL,
  zipcode VARCHAR(255) NOT NULL,
  longitude NUMERIC(17,10) NULL,
  latitude NUMERIC(17,10) NULL,
  selfie_photo VARCHAR(255) NOT NULL,
  identity_photo VARCHAR(255) NOT NULL,
  spouse_identity_photo VARCHAR(255) NULL,
  active BOOLEAN DEFAULT TRUE,
  created_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  created_user VARCHAR(255) DEFAULT 'system',
  updated_date TIMESTAMP NULL,
  updated_user VARCHAR(255)
);
