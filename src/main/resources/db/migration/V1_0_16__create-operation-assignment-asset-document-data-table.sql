CREATE TABLE IF NOT EXISTS operation_assignment_asset_document_data (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assignment_id BIGINT NOT NULL,
  vehicle_ownership_completed BOOLEAN DEFAULT FALSE,
  vehicle_registration_completed BOOLEAN DEFAULT FALSE,
  tax_notice_completed BOOLEAN DEFAULT FALSE,
  asset_code_brand VARCHAR(255) NULL,
  asset_brand_name VARCHAR(255) NULL,
  asset_code_model VARCHAR(255) NULL,
  asset_model_name VARCHAR(255) NULL,
  asset_code_variant VARCHAR(255) NULL,
  asset_variant_name VARCHAR(255) NULL,
  asset_year_made INT NULL,
  license_number VARCHAR(255) NULL,
  vehicle_ownership_number VARCHAR(255) NULL,
  vehicle_ownership_on_behalf_of VARCHAR(255) NULL,
  vehicle_ownership_chassis_number VARCHAR(255) NULL,
  vehicle_ownership_machine_number VARCHAR(255) NULL,
  vehicle_registration_number VARCHAR(255) NULL,
  vehicle_registration_expired_date VARCHAR(255) NULL,
  vehicle_registration_color VARCHAR(255) NULL,
  vehicle_registration_chassis_number VARCHAR(255) NULL,
  vehicle_registration_machine_number VARCHAR(255) NULL,
  tax_notice_number VARCHAR(255) NULL,
  tax_notice_expired_date VARCHAR(255) NULL,
  tax_notice_color VARCHAR(255) NULL,
  tax_notice_chassis_number VARCHAR(255) NULL,
  tax_notice_machine_number VARCHAR(255) NULL,
  active BOOLEAN DEFAULT TRUE,
  created_date TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
  created_user VARCHAR(255) DEFAULT 'system',
  updated_date TIMESTAMP NULL,
  updated_user VARCHAR(255),
  CONSTRAINT fk_operation_assignment_asset_document_data_operation_assignment_assignment_id FOREIGN KEY (assignment_id) REFERENCES operation_assignment (id)
);
